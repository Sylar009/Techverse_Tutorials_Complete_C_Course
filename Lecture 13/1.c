#include <stdio.h>

int main() {
    int positiveNum = 42; // Example of a positive integer
    int negativeNum = -42; // Example of a negative integer
    unsigned int unsignedNum = 500U; // Example of unsigned integer
    long int largeNum = 100000L; // Example of long integer

    printf("Positive Integer: %d\n", positiveNum);
    printf("Negative Integer: %d\n", negativeNum);
    printf("Unsigned Integer: %u\n", unsignedNum);
    printf("Long Integer: %ld\n", largeNum);

    return 0;
}